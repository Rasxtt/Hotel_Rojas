    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String rol = "";
        SistemaAdministrador SA = new SistemaAdministrador();
        SistemaRecepcionista SR = new SistemaRecepcionista();
        rol = autentificacion.login(
            jTextField2.getText(),
            new String(jPasswordField1.getPassword())
        );

        if (rol.equalsIgnoreCase("A")) {
            this.setVisible(false);
            SA.setVisible(true);
        }
        else if (rol.equalsIgnoreCase("R")) {
            this.setVisible(false);
            SR.setVisible(true);
        }
        else {
            JOptionPane.showMessageDialog(
            this,
            "Usuario o contraseña incorrecta",      // mensaje
            "Error",               // título de la ventana
            JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_jButton1ActionPerformed

