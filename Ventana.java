    private void escalarImagenLabel3() {
        ImageIcon icon = new ImageIcon(getClass().getResource("/Paquete/usuario.png"));
        Image img = icon.getImage();
        int w = jLabel3.getWidth();
        int h = jLabel3.getHeight();
        if (w > 0 && h > 0) {
            Image newImg = img.getScaledInstance(w, h, Image.SCALE_SMOOTH);
            jLabel3.setIcon(new ImageIcon(newImg));
        }
    }

